package model;

public class Nombre {
	
	//metadata
    private String type = "Nombre";

    private String nombre;

    public Nombre(){

    }
    public Nombre(String nombre){
        super();
        this.nombre = nombre;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

}
